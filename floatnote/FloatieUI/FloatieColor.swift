import UIKit
import SwiftUI

/// Floatie Color extension
extension Color {
    static let floatieBlue = Color("floatieBlue")
    static let floatieDarkBlue = Color("floatieDarkBlue")
    static let floatieDisabled = Color("floatieDisabled")
    static let floatieLightBlue = Color("floatieLightBlue")
}

/// Floatie UIColor extension
extension UIColor {
    static let floatieBlue = UIColor(named: "floatieBlue")
    static let floatieDarkBlue = UIColor(named: "floatieDarkBlue")
}
